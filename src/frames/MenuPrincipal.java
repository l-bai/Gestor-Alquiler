    public int verTamanyo(String nomTabla){
        int tamanyo=0;
        try {
            con.conecta();
            con.crearSentencia();
            con.cargaTabla(nomTabla);
            tamanyo = con.tamanyo();
            con.cerrarConexion();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error SQL. En " + nomTabla, JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error!!", JOptionPane.ERROR_MESSAGE);
        }
        return tamanyo;
    }

