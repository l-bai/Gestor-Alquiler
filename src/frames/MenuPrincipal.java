    private void jBalq_nuevo_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalq_nuevo_ActionPerformed
        //nuevo alquiler
        
        boolean hayVeh=false, hayClientes=true;
        try{
            con.conecta();
            con.crearSentencia();
            con.cargaTabla("Vehiculos");
            if(con.tamanyo()>0){
                hayVeh = true;
            }
            con.cargaTabla("Clientes");
            if(con.tamanyo()>0){
                hayClientes = true;
            }
            con.cerrarConexion();
        }
          catch (SQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error clientes!!", JOptionPane.ERROR_MESSAGE);
        }
       
        //se mandan las tres listas
        if(!hayClientes){
                    JOptionPane.showMessageDialog(null, "No hay conductores en el sistema", "Lista vacía", JOptionPane.ERROR_MESSAGE);

        }
        if(!hayVeh){
                    JOptionPane.showMessageDialog(null, "No hay vehículos en el sistema", "Lista vacía", JOptionPane.ERROR_MESSAGE);

        }
        if (hayClientes && hayVeh) {
            JFalq_nuevo alq = new JFalq_nuevo();
            alq.setVisible(true);
        }
    }//GEN-LAST:event_jBalq_nuevo_ActionPerformed

